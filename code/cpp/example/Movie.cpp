#include "Movie.h"

////////////////////////////////////////////////////////////
Movie::Movie(const string& title, const int priceCode) : title(title), priceCode(priceCode)
{
}

////////////////////////////////////////////////////////////
int Movie::getPriceCode() const
{
	return priceCode;
}

////////////////////////////////////////////////////////////
void Movie::setPriceCode(const int priceCode)
{
	this->priceCode = priceCode;
}

////////////////////////////////////////////////////////////
string Movie::getTitle() const
{
	return title;
}
